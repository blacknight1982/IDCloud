package com.id.cloud.delicacy.entities;

public class DelicacyCooktool {

	private int cookToolID;
	
	private String cookToolName;
	
	private String cookToolBrand;
	
	private String cookToolDescription;
	
	private String cookToolURL;

	public int getCookToolID() {
		return cookToolID;
	}

	public void setCookToolID(int cookToolID) {
		this.cookToolID = cookToolID;
	}

	public String getCookToolName() {
		return cookToolName;
	}

	public void setCookToolName(String cookToolName) {
		this.cookToolName = cookToolName;
	}

	public String getCookToolBrand() {
		return cookToolBrand;
	}

	public void setCookToolBrand(String cookToolBrand) {
		this.cookToolBrand = cookToolBrand;
	}

	public String getCookToolDescription() {
		return cookToolDescription;
	}

	public void setCookToolDescription(String cookToolDescription) {
		this.cookToolDescription = cookToolDescription;
	}

	public String getCookToolURL() {
		return cookToolURL;
	}

	public void setCookToolURL(String cookToolURL) {
		this.cookToolURL = cookToolURL;
	}
	
}
